fn check_civil_conversion(y: i32, m: u8, d: u8, date: i32) !void {
    try check_civil_conversion_impl(y, m, d, date, civil.civil32);
    try check_civil_conversion_impl(y, m, d, date, civil.civil64);
}

fn check_civil_conversion_impl(y: i32, m: u8, d: u8, date: i32, comptime impl: type) !void {
    errdefer std.debug.print("For {}\n", .{ date });
    try std.testing.expectEqual(date, impl.ymd_to_days(y, m, d));
    if (y > -5_877_611) {
        // though some dates in the year -5877611 are representable, Jan 1 is not.
        try std.testing.expectEqual(impl.year_to_days(y), impl.ymd_to_days(y, 1, 1));
        try std.testing.expectEqual(civil.year_to_days_alt(y), impl.year_to_days(y));
    }

    const ymd = impl.days_to_ymd(date);
    try std.testing.expectEqual(y, ymd.y);
    try std.testing.expectEqual(m, ymd.m);
    try std.testing.expectEqual(d, ymd.d);
    try std.testing.expectEqual(y, impl.days_to_year(date));
}

